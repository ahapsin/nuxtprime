                                h. BPR harus menyediakan dan melakukan kaji ulang atas jejak audit/log baik di tingkat
                                jaringan, sistem maupun aplikasi serta menetapkan jenis log (misalnya administrator log,
                                user log, system log), informasi yang harus dimasukkan ke dalam log, jangka waktu
                                penyimpanan atau kapasitas log dengan memperhatikan ketentuan yang berlaku untuk
                                keperluan penelusuran masalah.

                                6. Penanganan Insiden dalam Pengamanan Informasi
                                Hal-hal yang harus diperhatikan BPR dalam melakukan penanganan insiden dalam pengamanan
                                informasi antara lain :
                                a. insiden yang terjadi harus dapat diidentifikasi, dilaporkan, ditindaklanjuti,
                                didokumentasikan, dan dievaluasi untuk memastikan dilakukannya penanganan yang tepat dan
                                untuk mencegah terulangnya insiden;
                                b. BPR harus menetapkan prosedur penanganan insiden yang mengatur antara lain :
                                1) siapa yang harus melaporkan insiden;
                                2) jenis insiden yang harus dilaporkan;
                                3) alur pelaporan insiden (point of contact);
                                4) analisis atas insiden untuk mencegah terulangnya insiden; dan
                                5) pendokumentasian bukti terkait insiden dan tindak lanjutnya.
                                c. BPR perlu mempertimbangkan pembentukan tim khusus yang menangani insiden pengamanan
                                oleh TRIPI (Tim Respon Insiden dalam Pengamanan Informasi) sesuai dengan skala usaha dan
                                kompleksitas penyelenggaraan Teknologi Informasi BPR;
                                d. pegawai BPR, pegawai honorer, dan pegawai penyedia jasa Teknologi Informasi diminta
                                untuk melaporkan setiap kali menemukan indikasi atau potensi kelemahan pada Sistem
                                Elektronik sesuai kebijakan dan prosedur pelaporan insiden pengamanan. Kelemahan yang
                                perlu dilaporkan misalnya adanya virus dari surat elektronik yang masuk.

                                7. Rekam Cadang dan Uji Restore
                                Rekam cadang dan uji restore diperlukan untuk menjamin tersedianya data untuk
                                kelangsungan operasional BPR, baik dalam operasional secara rutin maupun apabila terjadi
                                gangguan kerusakan terhadap data sehingga kerugian yang lebih besar dapat dihindarkan.
                                Dalam hal ini rekam cadangdan uji restore dimaksud meliputi data, file, aplikasi, sistem
                                operasi, dan dokumen lainnya, harus disimpan di lokasi/gedung yang terpisah. Hal-hal
                                tersebut di atas adalah beberapa contoh kontrol dan teknologi yang dapat digunakan untuk
                                membantu pengamanan informasi.

                                8. Retensi Data
                                Retensi data dilakukan untuk mempertahankan integritas data yang di rekam cadang dalam
                                jangka waktu tertentu. Jangka waktu retensi data disesuaikan dengan ketentuan peraturan
                                perundang-undangan yang mengatur mengenai dokumen perusahaan.

                                9. Lainnya
                                Pengamanan informasi juga perlu diterapkan dalam aspek lain seperti pengembangan dan
                                pengadaan Sistem Elektronik termasuk Aplikasi Inti Perbankan, jaringan komunikasi data,
                                rencana pemulihan bencana, dan kegiatan kerja sama dengan penyedia jasa Teknologi
                                Informasi atau penyedia Aplikasi Inti Perbankan.

