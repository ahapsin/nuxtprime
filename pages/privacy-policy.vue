                                3. Pengamanan Fisik dan Lingkungan
                                a. fasilitas pemrosesan informasi yang penting (misalnya mainframe, peladen, komputer,
                                perangkat jaringan aktif) juga harus diberikan pengamanan secara fisik dan lingkungan
