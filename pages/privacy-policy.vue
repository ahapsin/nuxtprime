                                4) pemilik user-id terutama dari pegawai BPR, pegawai honorer, dan pegawai penyedia jasa
                                Teknologi Informasi harus menandatangani pernyataan tanggung jawab atau perjanjian
                                penggunaan user-id dan password saat menerima user-id dan password;
                                5) password standar (default password) yang dimiliki oleh sistem operasi, sistem
