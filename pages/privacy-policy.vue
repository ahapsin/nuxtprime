                                5. Pengamanan Operasional Teknologi Informasi
                                Hal-hal yang harus diperhatikan dalam pengamanan operasional Teknologi Informasi antara
                                lain :
                                a. informasi dan perangkat lunak harus dibuatkan rekam cadang dan prosedur pemulihan
                                yang teruji sesuai dengan tingkat kepentingannya;
                                b. BPR perlu mengantisipasi dan menerapkan pengendalian pengamanan yang memadai atas
                                kelemahan sistem operasi, sistem aplikasi, Pangkalan Data dan jaringan, termasuk ancaman
                                dari pihak yang tidak berwenang seperti virus, trojan horse, worms, spyware,
                                Denial-of-Service (DoS), war driving, spoofing, dan logic bomb;
                                c. BPR harus memiliki kebijakan dan prosedur pengkinian anti-virus dan patch serta
                                memastikan pelaksanaannya;
                                d. BPR harus membuat prosedur yang mencakup identifikasi patch yang ada, melakukan
                                pengujian, dan menginstalasinya jika memang dibutuhkan;
                                e. BPR harus memelihara catatan dari versi perangkat lunak yang digunakan dan memantau
                                secara rutin informasi tentang pengkinian (enhancement) produk, masalah keamanan, patch
                                atau upgrade, atau permasalahan lain yang sesuai dengan versi perangkat lunak yang
                                digunakan;
                                f. BPR harus menetapkan penggunaan enkripsi dengan menggunakan teknik kriptografi
                                tertentu dalam mengamankan proses transmisi informasi yang sensitif, khususnya yang
                                melalui jaringan di luar jaringan komunikasi BPR, sesuai dengan perkembangan teknologi
                                terkini. Penggunaan teknik kriptografi tersebut antara lain ditujukan untuk menjaga dan
                                memastikan kerahasiaan (confidentiality), integritas (integrity), keaslian
                                (authenticity), dan non-repudiation. Teknik yang dapat dipertimbangkan antara lain
